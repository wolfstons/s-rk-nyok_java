package teljesOOP;

public class Megoldas {

    private int x, y;

    public Megoldas(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

  
    public String allapot() {
        return "Megoldas{" + "x=" + x + ", y=" + y + '}';
    }

}
